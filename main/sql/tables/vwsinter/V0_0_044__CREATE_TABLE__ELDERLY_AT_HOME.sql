IF NOT EXISTS(SELECT * FROM sys.sequences WHERE object_id = OBJECT_ID(N'[dbo].[SEQ_VWSINTER_ELDERLY_AT_HOME]') AND type = 'SO')
CREATE SEQUENCE SEQ_VWSINTER_ELDERLY_AT_HOME
  START WITH 1
  INCREMENT BY 1;
GO

CREATE TABLE VWSINTER.ELDERLY_AT_HOME(
  [ID] INT PRIMARY KEY NOT NULL DEFAULT (NEXT VALUE FOR [dbo].[SEQ_VWSINTER_ELDERLY_AT_HOME]),
  [DATE_OF_REPORT] DATETIME NULL,
  [DATE_OF_STATISTIC_REPORTED] DATETIME NULL,
  [SECURITY_REGION_CODE] VARCHAR(100) NULL,
  [TOTAL_CASES_REPORTED] INT NULL,
  [TOTAL_DECEASED_REPORTED] INT NULL,
  [DATE_LAST_INSERTED]  DATETIME DEFAULT GETDATE()
);

IF NOT EXISTS(SELECT * FROM sys.indexes WHERE NAME='IX_VWSINTER_ELDERLY_AT_HOME')
    CREATE NONCLUSTERED INDEX IX_VWSINTER_ELDERLY_AT_HOME
    ON VWSINTER.ELDERLY_AT_HOME(DATE_LAST_INSERTED, DATE_OF_STATISTIC_REPORTED, SECURITY_REGION_CODE);
GO