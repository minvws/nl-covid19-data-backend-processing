IF NOT EXISTS(SELECT * FROM sys.sequences WHERE object_id = OBJECT_ID(N'[dbo].[SEQ_VWSINTER_VWS_RESTRICTIONS_MAPPING]') AND type = 'SO')
CREATE SEQUENCE SEQ_VWSINTER_VWS_RESTRICTIONS_MAPPING
  START WITH 1
  INCREMENT BY 1;
GO

CREATE TABLE VWSINTER.VWS_RESTRICTIONS_MAPPING(
    [ID] INT PRIMARY KEY NOT NULL DEFAULT (NEXT VALUE FOR [dbo].[SEQ_VWSINTER_VWS_RESTRICTIONS_MAPPING]),
    [IDENTIFIER] INT NULL,
    [TARGET_REGION] VARCHAR(100) NULL,
    [ESCALATION_LEVEL] INT NULL,
    [CATEGORY_ID] VARCHAR(100) NULL,
    [RESTRICTION_ORDER] INT NULL,
    VALID_FROM DATETIME NULL,
	  [DATE_LAST_INSERTED]  DATETIME DEFAULT GETDATE()
);

CREATE NONCLUSTERED INDEX IX_VWS_RESTRICTIONS_MAPPING_INTER
ON VWSINTER.VWS_RESTRICTIONS_MAPPING(DATE_LAST_INSERTED, TARGET_REGION)
INCLUDE (IDENTIFIER);