GO

CREATE TABLE VWSDEST.SEWER_MEASUREMENTS_PER_RWZI (
	[ID] INT PRIMARY KEY NOT NULL DEFAULT (NEXT VALUE FOR [dbo].[SEQ_VWSDEST_SEWER_MEASUREMENTS_PER_RWZI_2021]),
