	[DATE_MEASUREMENT_UNIX] BIGINT NULL,
	[WEEK] INT NULL,
	[RWZI_AWZI_CODE] VARCHAR(100) NULL,
	[RWZI_AWZI_NAME] VARCHAR(100) NULL,
	RNA_FLOW_PER_100000 DECIMAL(16,2) NULL,
