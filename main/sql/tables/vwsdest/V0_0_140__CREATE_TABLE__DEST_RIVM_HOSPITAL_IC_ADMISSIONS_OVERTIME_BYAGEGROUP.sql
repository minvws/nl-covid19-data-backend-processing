IF NOT EXISTS(SELECT * FROM sys.sequences WHERE object_id = OBJECT_ID(N'[dbo].[SEQ_VWSDEST_RIVM_HOSPITAL_IC_ADMISSIONS_OVERTIME_BYAGEGROUP]') AND type = 'SO')
