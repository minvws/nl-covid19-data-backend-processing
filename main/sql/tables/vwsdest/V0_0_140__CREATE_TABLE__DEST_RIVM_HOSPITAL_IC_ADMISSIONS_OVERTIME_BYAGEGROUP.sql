    IC_ADMISSION_NOTIFICATION INT NULL,
    IC_ADMISSION INT NULL,    
	[HOSPITAL_ADMISSION_PER1M] decimal(19,1) NULL,
	[IC_ADMISSION_PER1M] decimal(19,1) NULL,
	[DATE_LAST_INSERTED] DATETIME DEFAULT GETDATE()
);

