CREATE OR ALTER PROCEDURE [dbo].[SP_HOSPITAL_ADMISSIONS_PER_MUNICIPALITY_ARCHIVE_DEST]
AS
BEGIN

    WITH SELECTED_TO_BE_ARCHIVED AS (
    SELECT 
       [DATE_OF_REPORT]
      ,[DATE_OF_REPORT_UNIX]
      ,[MUNICIPALITY_CODE]
      ,[MUNICIPALITY_NAME]
      ,[TOTAL_COUNTS_PER_DAY]
      ,[MOVING_AVERAGE_HOSPITAL]
      ,[DATE_LAST_INSERTED]
    FROM [VWSDEST].[HOSPITAL_ADMISSIONS_PER_MUNICIPALITY]
    WHERE DATE_LAST_INSERTED IN (
        SELECT SubQueryC.DATE_LAST_INSERTED
        FROM(
        SELECT SubQueryB.DATE_LAST_INSERTED, SubQueryB.RANKING
        FROM (
            SELECT SubQueryA.DATE_LAST_INSERTED, rank() OVER(ORDER BY DATE_LAST_INSERTED DESC) RANKING
            FROM (
            SELECT DISTINCT DATE_LAST_INSERTED
            FROM [VWSDEST].[HOSPITAL_ADMISSIONS_PER_MUNICIPALITY]) SubQueryA
        ) SubQueryB
        WHERE SubQueryB.RANKING > 2)SubQueryC))

    INSERT INTO VWSARCHIVE.HOSPITAL_ADMISSIONS_PER_MUNICIPALITY
        ([DATE_OF_REPORT]
      ,[DATE_OF_REPORT_UNIX]
      ,[MUNICIPALITY_CODE]
      ,[MUNICIPALITY_NAME]
      ,[TOTAL_COUNTS_PER_DAY]
      ,[MOVING_AVERAGE_HOSPITAL]
      ,[DATE_LAST_INSERTED])
     SELECT
        [DATE_OF_REPORT]
      ,[DATE_OF_REPORT_UNIX]
      ,[MUNICIPALITY_CODE]
      ,[MUNICIPALITY_NAME]
      ,[TOTAL_COUNTS_PER_DAY]
      ,[MOVING_AVERAGE_HOSPITAL]
      ,[DATE_LAST_INSERTED]
    FROM SELECTED_TO_BE_ARCHIVED;

    DELETE FROM VWSDEST.HOSPITAL_ADMISSIONS_PER_MUNICIPALITY
    WHERE DATE_LAST_INSERTED IN (
        SELECT SubQueryC.DATE_LAST_INSERTED
        FROM(
        SELECT SubQueryB.DATE_LAST_INSERTED, SubQueryB.RANKING 
        FROM (
            SELECT SubQueryA.DATE_LAST_INSERTED, rank() OVER(ORDER BY DATE_LAST_INSERTED DESC) RANKING
            FROM (
            SELECT DISTINCT DATE_LAST_INSERTED
            FROM VWSDEST.HOSPITAL_ADMISSIONS_PER_MUNICIPALITY) SubQueryA
        ) SubQueryB
        WHERE SubQueryB.RANKING > 2)SubQueryC);
END;