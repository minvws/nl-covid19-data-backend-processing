CREATE OR ALTER VIEW VWSDEST.V_POSITIVE_TESTED_PEOPLE_PER_MUNICIPALITY_BACK_UP AS
SELECT 
    ROW_NUMBER() OVER (ORDER BY DATE_OF_REPORT_UNIX ASC) AS ID,
    DATE_OF_REPORT,
	DATE_OF_REPORT_UNIX,
	MUNICIPALITY_CODE,
	MUNICIPALITY_NAME,
	INFECTED_DAILY_TOTAL,
	INFECTED_DAILY_INCREASE,
	DATE_LAST_INSERTED
FROM VWSDEST.POSITIVE_TESTED_PEOPLE_PER_MUNICIPALITY
WHERE DATE_LAST_INSERTED = (SELECT max(DATE_LAST_INSERTED) 
                            FROM VWSDEST.POSITIVE_TESTED_PEOPLE_PER_MUNICIPALITY)